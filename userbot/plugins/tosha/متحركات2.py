@bot.on(admin_cmd(outgoing=True, pattern="ش1$"))
@bot.on(sudo_cmd(pattern="ش1$", allow_sudo=True))
async def fygif(Ralls):
    if Ralls.fwd_from:
        return
    Ti = await rd(Ralls)
    if fy_gif:
        Ralls_caption = f"**{TSHE}**\n"
        Ralls_caption += f"𓍹ⵧⵧⵧⵧⵧⵧⵧⵧⵧ𝐑𝐀𝐈𝐈𝐒ⵧⵧⵧⵧⵧⵧⵧⵧⵧ𓍻\n"
        Ralls_caption += f"**↫ المتـحركه الاولى 𓆰.**"
        await Ralls.client.send_file(
            Ralls.chat_id, fy_gif, caption=Ralls_caption, reply_to=Ti
        )

